"""
ch05 · 示例 1:编号不是含义 —— 为什么不能直接拿 token id 喂模型
=================================================================
上一章我们把文字切成 token、编上号。但有个大问题:

    编号只是「身份证号」,本身不带任何含义。

「猫」是 3 号、「狗」是 7 号、「桌子」是 8 号——可在编号的世界里,
3 和 7 的距离(4),竟然比 7 和 8 的距离(1)还远。
难道「猫」离「狗」比「狗」离「桌子」更远?显然荒唐。

这一节先把这个问题摆清楚,再看一个最直接的补救办法(one-hot)为什么也不行。
"""
import numpy as np

# 假设词表里有这几个词,各给一个编号
VOCAB = ["猫", "狗", "桌子", "椅子"]
stoi = {w: i for i, w in enumerate(VOCAB)}
print("词表与编号:", stoi)

# ---------- 问题 1:编号之间的「距离」毫无意义 ----------
print("\n【问题 1】编号的大小 / 距离不代表含义远近")
pairs = [("猫", "狗"), ("狗", "桌子")]
for a, b in pairs:
    d = abs(stoi[a] - stoi[b])
    print(f"  |{a}({stoi[a]}) - {b}({stoi[b]})| = {d}")
print("  -> 编号上『猫·狗』距离 1,『狗·桌子』距离 1,但语义上猫狗明明更近。")
print("     编号是随便编的,数值大小纯属巧合,不能拿来算含义。")

# ---------- 直接补救:one-hot(独热编码) ----------
# 既然不能用大小,那就让每个词「各占一格」:第 i 号词 = 第 i 位是 1、其余全 0。
def one_hot(idx, n):
    v = np.zeros(n)
    v[idx] = 1.0
    return v

V = len(VOCAB)
print("\n把每个词变成 one-hot 向量(各占一格,谁也不比谁大):")
for w in VOCAB:
    print(f"  {w} -> {one_hot(stoi[w], V).astype(int)}")

# ---------- 问题 2:one-hot 里任意两个词都「一样远」 ----------
print("\n【问题 2】one-hot 解决了大小,却让所有词彼此等距")
cat, dog, table = one_hot(stoi["猫"], V), one_hot(stoi["狗"], V), one_hot(stoi["桌子"], V)
def dist(u, v):
    return float(np.linalg.norm(u - v))
print(f"  距离(猫, 狗)  = {dist(cat, dog):.3f}")
print(f"  距离(猫, 桌子) = {dist(cat, table):.3f}")
print("  -> 任意两个不同词的 one-hot 距离都是 √2 ≈ 1.414,完全一样。")
print("     模型还是看不出『猫更像狗』。而且词表多大,向量就多长(5万词=5万维),太浪费。")

print("""
小结:
  · 直接用编号:数值大小是假的,不能代表含义。
  · one-hot:解决了大小问题,但所有词两两等距、且维度爆炸,依然没有「含义」。

我们真正想要的是:用一个【不太长、稠密的小数向量】表示每个词,
并且让【意思相近的词,向量也相近】。这就是 Embedding(词嵌入)——下一个示例登场。
""")
