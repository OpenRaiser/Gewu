"""
ch11 · 示例 1:LoRA 的核心思想 —— 用两个「瘦矩阵」代替一个「胖矩阵」
====================================================================
大模型微调的痛点:一个权重矩阵 W 动辄是 4096×4096,几千万个数。
要把它整个重新训练一遍(全参微调),既费显存又费算力,还得为每个任务
存一整份新权重。

LoRA(Low-Rank Adaptation)的观察:微调时,权重的「改动量」ΔW 往往
不需要那么「丰富」——它可以用一个**低秩**矩阵很好地近似。

   原本:  W'  =  W + ΔW            ΔW 和 W 一样大(d×d),要学 d² 个数
   LoRA:  W'  =  W + B·A           A 是 (r×d),B 是 (d×r),r 很小
                 └─冻结─┘ └可训练┘   只学 2·d·r 个数,r<<d 时省得惊人

本示例不训练,只把「省了多少参数」和「低秩为什么够用」用数字摆出来。
"""
import numpy as np

np.set_printoptions(precision=3, suppress=True)
rng = np.random.default_rng(0)

# ---------- 1. 参数量对比 ----------
print("===== 参数量对比:全参 vs LoRA =====")
for d in [512, 4096]:
    full = d * d
    for r in [4, 8]:
        lora = 2 * d * r
        print(f"  矩阵 {d}×{d}:  全参微调 {full:>12,} 个  |  "
              f"LoRA(r={r}) {lora:>10,} 个  |  仅为 {lora/full*100:.2f}%")
print("-> r 取个位数,可训练参数常常降到原来的 1% 以下。这就是 LoRA 省钱的来源。")

# ---------- 2. 为什么「低秩」常常够用:看一个真实的「改动量」 ----------
# 造一个「微调改动量」ΔW:假设它本质上是低秩的(现实中近似成立)。
d = 64
r_true = 3
B_true = rng.standard_normal((d, r_true))
A_true = rng.standard_normal((r_true, d))
dW = B_true @ A_true * 0.1          # 真实改动量:秩只有 3
print("\n===== 用低秩去近似一个改动量 ΔW (64×64) =====")

# 用 SVD 看 ΔW 的「奇异值」:能量集中在前几个,说明它确实低秩。
U, S, Vt = np.linalg.svd(dW)
print("ΔW 的前 8 个奇异值:", S[:8])
print("第 4 个及以后基本为 0 -> ΔW 的'有效维度'就是 3,天生低秩。")

# 用秩 r 的 LoRA 形式去重建,看误差随 r 怎么变
print("\n用秩 r 重建 ΔW 的相对误差:")
for r in [1, 2, 3, 4, 8]:
    approx = (U[:, :r] * S[:r]) @ Vt[:r, :]    # 截断到前 r 个成分
    err = np.linalg.norm(dW - approx) / np.linalg.norm(dW)
    print(f"  r={r}:  相对误差 = {err:.4f}")
print("-> 一旦 r 达到真实秩(3),误差就归零。选对 r,低秩近似几乎无损。")

print("""
小结:
  · LoRA = 冻结原权重 W,只额外学一个低秩增量 B·A(两个瘦矩阵)。
  · 可训练参数从 d² 降到 2·d·r,r<<d 时常常只剩原来的不到 1%。
  · 底气在于:微调的「改动量」ΔW 经验上接近低秩(奇异值能量集中在前几个)。
  · 选的秩 r 一旦覆盖了改动量的有效维度,低秩近似几乎无损。
  · 好处:省显存、训得快、每个任务只需存一份小小的 A/B,随时插拔切换。
""")
