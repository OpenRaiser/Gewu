"""
ch06 · 示例 1:为什么需要「注意力」?
======================================
上一章我们把每个词查成了一个含义向量。可这些向量是【孤立】的——
「苹果」永远是同一个向量,不管它出现在:

    "我吃了一个苹果"      (水果)
    "苹果发布了新手机"    (公司)

同一个词,在不同上下文里意思天差地别。光看词本身的向量,模型分不清。
怎么办?让每个词【看一看句子里的其他词】,根据上下文调整自己的表示。
这就是「注意力(attention)」最朴素的动机。

本示例不引入 Q/K/V,只用最直白的方式说明一件事:
  「一个词的新表示 = 它和上下文里每个词的相关度,作为权重,加权汇总所有词。」
"""
import numpy as np

np.set_printoptions(precision=3, suppress=True)

# 一句话 4 个词,每个词先有一个 3 维的「原始含义向量」(假装是上一章查表得到的)
WORDS = ["我", "吃", "苹果", "了"]
X = np.array([
    [1.0, 0.0, 0.2],   # 我
    [0.2, 1.0, 0.1],   # 吃
    [0.3, 0.8, 0.9],   # 苹果(和「吃」有点像 -> 偏食物义)
    [0.0, 0.1, 0.0],   # 了
])

print("原始词向量 X(每行一个词,彼此孤立):")
for w, v in zip(WORDS, X):
    print(f"  {w:<3} {v}")

# ---------- 第 1 步:算「相关度」----------
# 最朴素的相关度 = 两个词向量的点积(越像,点积越大)。
# 对每个词,算它和句子里所有词(含自己)的点积。
scores = X @ X.T          # (4,4):scores[i,j] = 第 i 词 与 第 j 词 的相关度
print("\n相关度矩阵 scores = X · Xᵀ(第 i 行 = 第 i 个词对所有词的相关度):")
print(scores)

# ---------- 第 2 步:把相关度变成「权重」(每行加起来=1)----------
def softmax(v):
    e = np.exp(v - v.max())
    return e / e.sum()

weights = np.array([softmax(row) for row in scores])
print("\n权重 weights = 对每行做 softmax(每行和为 1,代表「注意力分配」):")
print(weights)

# ---------- 第 3 步:用权重加权汇总,得到「带上下文」的新表示 ----------
Y = weights @ X           # (4,3):每个词的新向量 = 所有词向量的加权平均
print("\n新表示 Y = weights · X(每个词都「混入」了上下文):")
for w, v in zip(WORDS, Y):
    print(f"  {w:<3} {v}")

# ---------- 看「苹果」到底关注了谁 ----------
i = WORDS.index("苹果")
print(f"\n『苹果』对各词的注意力权重:")
for w, p in zip(WORDS, weights[i]):
    bar = "█" * int(p * 30)
    print(f"  {w:<3} {p:.3f} {bar}")
print("  -> 『苹果』给了『吃』很高的权重,于是它的新向量更偏「食物」义。")
print("     如果句子是「苹果发布手机」,它就会更关注「发布/手机」,偏「公司」义。")

print("""
小结:
  · 注意力的核心动作:每个词看所有词,算出相关度 -> 归一成权重 -> 加权汇总。
  · 这样每个词的新表示就【带上了上下文】,同一个词在不同句子里能有不同含义。
  · 但这里有个缺陷:相关度直接用「原始向量点积」,词只能用「原本的样子」去匹配,
    没法分别表达「我想找什么」和「我能提供什么」。下一节用 Q/K/V 解决它。
""")
