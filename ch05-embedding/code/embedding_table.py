"""
ch05 · 示例 2:Embedding = 一张可查的「含义向量表」
=====================================================
上一节的结论:我们想用一个【短、稠密、能表达含义】的向量代表每个词。

办法朴素得不能再朴素:**准备一张表**。
表有 V 行(词表大小)、D 列(每个词用 D 个小数表示),第 i 行就是第 i 号词的向量。
这张表就叫 **Embedding 矩阵**,每个词的向量叫它的 **embedding(词嵌入)**。

「查」一个词的向量,就是「取出表的第 i 行」——就这么简单。
本节还会揭示一个关键真相:**one-hot 向量 × Embedding 矩阵 = 取出对应那一行**。
所以上一节的 one-hot 不是白费,它正是「查表」这个动作的数学写法。
"""
import numpy as np

VOCAB = ["猫", "狗", "桌子", "椅子"]
stoi = {w: i for i, w in enumerate(VOCAB)}
V = len(VOCAB)
D = 3  # 每个词用 3 个小数表示(真实 GPT 里是几百~上万维)

# 先随机初始化一张表(真实训练里,这些数字是「学」出来的;这里手填几个好看的值)
# 故意让「猫·狗」两行接近,「桌子·椅子」两行接近,演示「相近词向量相近」
np.random.seed(0)
E = np.array([
    [0.9, 0.8, 0.1],   # 猫
    [0.8, 0.9, 0.2],   # 狗
    [0.1, 0.2, 0.9],   # 桌子
    [0.2, 0.1, 0.8],   # 椅子
])
print(f"Embedding 矩阵 E:{V} 行(词)× {D} 列(每词 {D} 维向量)")
for w in VOCAB:
    print(f"  {w}({stoi[w]}) -> {E[stoi[w]]}")

# ---------- 查表方式一:直接按行号索引(代码里就这么用) ----------
print("\n【查表方式一】按行号直接取——E[i] 就是第 i 号词的向量")
for w in ["猫", "桌子"]:
    print(f"  E[{stoi[w]}]  ({w}) = {E[stoi[w]]}")

# ---------- 查表方式二:one-hot 乘矩阵(揭示「查表」的数学本质) ----------
def one_hot(idx, n):
    v = np.zeros(n)
    v[idx] = 1.0
    return v

print("\n【查表方式二】one-hot 向量 × E,得到的正是同一行")
oh_cat = one_hot(stoi["猫"], V)
row = oh_cat @ E                      # (V,) @ (V,D) -> (D,)
print(f"  猫的 one-hot {oh_cat.astype(int)} @ E = {row}")
print(f"  和 E[{stoi['猫']}] 一致吗?{np.allclose(row, E[stoi['猫']])}")
print("  -> 『查表取第 i 行』== 『用第 i 个 one-hot 去乘矩阵』。两者完全等价。")
print("     真实代码里当然用索引(快得多),但理解成矩阵乘法,能看清它是网络的一层。")

# ---------- 一次查一批(一个句子) ----------
print("\n一次查一整句:把每个词的向量按顺序摞起来")
sentence = ["猫", "狗", "椅子"]
ids = [stoi[w] for w in sentence]
batch = E[ids]                        # 花式索引:一次取多行 -> (句长, D)
print(f"  句子 {sentence} 的编号 {ids}")
print(f"  查出的向量组 shape = {batch.shape}(3 个词 × {D} 维)")
print(batch)

print("""
小结:
  · Embedding 矩阵 = V 行 × D 列的查找表,第 i 行 = 第 i 号词的稠密向量。
  · 「查表」既可写成按行索引 E[i],也等价于 one-hot @ E —— 它本质是网络的一层。
  · 一个句子 = 一串编号 -> 一叠向量 (句长 × D),这就是喂进后续网络的真正输入。

但现在这张表是我手填的。怎么判断「猫真的离狗更近」?需要一把尺子——
下一个示例:用【余弦相似度】来量两个向量「像不像」。
""")
